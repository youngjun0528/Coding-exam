# 2진수 변환

a = int(input())

print("2진수 : " + bin(a))
print("8진수 : " + oct(a))
print("16진수 : " + hex(a))

print("2진수 : " + format(a, '#b'))
print("8진수 : " + format(a, '#o'))
print("16진수 : " + format(a, '#x'))

print("2진수 : " + format(a, 'b'))
print("8진수 : " + format(a, 'o'))
print("16진수 : " + format(a, 'x'))
